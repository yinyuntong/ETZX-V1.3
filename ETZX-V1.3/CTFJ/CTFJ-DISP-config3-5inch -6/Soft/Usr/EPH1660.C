void MasterSPI_Send_Eph1660_Command(uint8 *Databuf,uint8 l)
{
	MCUSndFlag=1;
	MCUSndDataLength=l;
	MCUSndDataPosition=0;
	MCUSndDataBuff=Databuf;
	MCUSndCmdFinishFlag=0;
	SPSTAT |= (SPIF|WCOL);	//清SPI中断标志
	SPDAT=*Databuf;		
	WKICOM=0;	//单片机开始发送命令
}

